                          <p class="narrow text-center">
														अहमदनगर जिल्ह्यातील, अकोले या तालुक्याचे ठिकाण असूनही अतिशय छोट्याशा खेड्यात, एका गरीब, कोकणस्थ ब्राह्मण कुटुंबात श्री. अनिल यांचा जन्म सहस्रबुद्धे घराण्यात 24 ऑगस्ट 1947 अर्थात श्रावण शुक्ल अष्टमी शके 1869 मध्ये झाला. त्यांच्या वडिलांचे नाव श्री. नागेश केशव सहस्रबुद्धे तर मातोश्रींचे नाव सौ. आनंदी नागेश सहस्रबुद्धे असे होते. श्री. नागेश व्हिलेज पोस्टमन म्हणून टपाल खात्यात नोकरीला होते. ते 1964 साली पोस्टमन म्हणून निवृत्त झाले. सौ. आनंदी या पूर्वाश्रमीच्या दुर्गा श्रीधर काणे होत्या, त्या गृहिणीपद सांभाळत होत्या. बालवयातच त्यांनी स्वातंत्र्यलढ्यात भाग घेतला होता. विवाहानंतर अकोले गावातही त्यांचे सामाजिक कार्य सुरू होते. महिला मंडळ उभारणी, विडी कारखान्यात जाऊन प्रौढ शिक्षण वर्ग घेणे आणि वैद्यकीय ज्ञान असल्याने व विशेषतः लहान मुलांच्या दुखण्याबाण्याचे ज्ञान असल्याने गावात सर्व जातीजमातीच्या समाजात जाऊन वैद्यकीय सेवा त्या करीत असत. नागेश व आनंद यांना नलिनी, शशिकला, अशोक, अनिल, रमेश व विद्या तथा नंदा तीन मुली व तीन मुले अशी अपत्ये होती. आठ जणांचे कुटुंब, तुटपुंजी मिळकत त्यामुळे हलाखीच्या परिस्थितीवर मात करण्यासाठी आई व मुले काही ना काही काम करीत व कसेबसे भागवित. सौ. आनंदी ह्या जेवणाचे डबे करून देणे व घरीही जेवायला खानावळीप्रमाणे देण्याचे काम करीत असत. त्यांनी ‘काटे आणि गरे’ असे आत्मचरित्र लिहिले आहे. स्वतः श्री. अनिल यांनी जेवणाचे डबे पोचविणे, घागरीने नदीवरून पाणी वाहणे, घरांना चुन्याचा रंग देणे, वर्तमानपत्र टाकणे अशी कामे करीत 1964 मध्ये अकरावी एस.एस.सी.पर्यंतचे शिक्षण पूर्ण केले. महाविद्यालयात जाऊन शिक्षण घेणे शक्य नव्हते; म्हणून त्यांनी श्रीरामपूर येथे राहून टीचर्स डिप्लोमा पूर्ण केला. वयाची अठरा वर्षे पूर्ण होण्यापूर्वीच ते अकोले येथील, स्वतःचे शिक्षण पूर्ण केलेल्या शाळेत, अर्थात ‘हिंद सेवा मंडळ’ या शिक्षण संस्थेचे, अकोले तालुक्यातील पहिले हायस्कूल ‘मॉडर्न हायस्कूल’मध्ये प्रथम शिपाई व दुसर्‍याच दिवशीपासून शिक्षक म्हणून सेवा करू लागले. शिक्षक म्हणून काम करीतच त्यांनी पुणे विद्यापीठाच्या ‘एम.ए.’ पदवीपर्यंतचे शिक्षण बह्यस्थ विद्यार्थी 1975 पर्यंत पूर्ण केले. दरम्यान 1971-72 मध्ये त्यांना शासकीय योजनेनुसार डेप्युटेशनवर ‘बी.एड.’ करण्याची संधी मिळाली. या सर्व प्रवासात त्यांना पुरुषोत्तम टाकळकर व त्यांच्या सौ. प्रमिला टाकळकर, गणेश सोमणी व सौ. सरस्वती सोमणी, पू. गुरुवर्य हरिभाऊ फडके सर व त्यांचे सहकारी शिक्षक, श्री. बन्सीशेठजी मुंदडा व सौ. अयोध्या मुंदडा व प्रामुख्याने त्यांची भगिनी माई अर्थात शशिकला सहस्रबुद्धे अर्थात विवाहोत्तर सौ. मधुकांता गाडगीळ या सर्वांनी मोठ्या प्रेमाने परोपरीने सांभाळले. डॉ. गंगाधर मोरजे यांच्या मार्गदर्शनाखाली ‘चाळीसगाव डांगाण परिसर ः सांस्कृतिक वाङ्मयीन व भाषिक अभ्यास’ या विषयावर अंतरज्ञानशाखीय स्वरूपात क्षेत्रीय अभ्यास करून पुणे विद्यापीठाची पीएच.डी. 1979-80 मध्ये त्यांनी संपादन केली.
													 येथवरच्या प्रवासात 1972 साली कल्याण येथील श्री. विनायक महादेव नातू व सौ. शारदा विनायक नातू यांची कन्या शोभा हिच्याशी त्यांचा विवाह झाला. सौ. उषा अनिल सहस्रबुद्धे यांना सप्टेंबर 1973 मध्ये चि. स्वप्नील हा पुत्र व 1978 मध्ये कु. स्फूर्ति ही कन्या झाली.
