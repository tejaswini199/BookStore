													<p class="narrow text-center">
														अनिल सहस्रबुद्धे यांनी ‘हिंद सेवा मंडळ’ या शिक्षण संस्थेत 1965 जुलैपासून 31 ऑगस्ट 2007 पर्यंत तब्बल बेचाळीस वर्षे माध्यमिक, उच्च माध्यमिक, महाविद्यालयीन, पदव्युत्तर अशा स्तरांवर अध्यापन केले. या कालावधीत गणित, शास्त्र, हिंदी, इंग्रजी व मराठी या विषयांचे त्यांनी अध्यापन केले. बावीस वर्षे त्यांनी मराठीचे प्राध्यापक, संशोधन मार्गदर्शक, उद्बोधक, मराठी विभाग प्रमुख म्हणून सेवा केली. अखेरच्या टप्प्यात पेमराज सारडा महाविद्यालयाचे प्राचार्य म्हणून त्यांनी धुरा सांभाळली.
 													 लहानपणापासूनच डॉ. सहस्रबुद्धे यांना पोहणे, झाडावर चढणे, डोंगरदर्‍यांतून भटकणे, देशपर्यटन, चित्रकला, मातीच्या मूर्ती करणे, नाट्याभिनय करणे, नृत्य, नृत्यनाट्य दिग्दर्शन, विशेषतः लोकनृत्य, वक्तृत्व, सामाजिक व सांस्कृतिक सेवा आणि कीर्तन ऐकणे याचा छंद आहे. तो त्यांनी विकसित केला. याचबरोबर लेखन हा त्यांचा ध्यास आणि श्वास आहे. त्यांनी लेखनछंदही विकसित केला. वाङ्मयाच्या सर्व प्रकारांमध्ये त्यांनी मोठ्या क्षमतेने लेखन केले.
 													 ‘राष्ट्र सेवा दल’ या संघटनेचे श्री. पगारे यांच्या मार्गदर्शनाखाली सैनिक व मुख्य शिक्षक म्हणून त्यांनी बालवयात इ. 4 थी ते 8 वीपर्यंत काम केले. राष्ट्र सेवा दलाच्या कलापथकात पोवाडे गाणे, नाट्यछटा सादर करणे, छोट्या संवादात काम करणे अशा सहभागातून समाज व सांस्कृतिक प्रबोधनात सहभाग नोंदवला. याच काळात सर्वोदयाच्या संस्कार साधनेत त्यांनी प्रभातफेर्‍या काढणे, साष्टांग नमस्कार स्पर्धेत भाग यात सहभाग नोंदवला. इ. 8 वीच्या अखेरच्या परीक्षेनंतर त्यांचा राष्ट्रीय स्वयंसेवक संघाच्या कै. दामुअण्णा दाते यांच्या संपर्कात आले आणि संघाचे संघटनात्मक कार्य करू लागले. संघाचे ओटीसी प्रशिक्षण मे 1962 मध्ये प्रथम वर्ष स्वरूपात पूर्ण केले. संघाच्या नियोजित उपक्रमात त्यांनी शाखा मुख्य शिक्षक म्हणून कार्य केले. आणीबाणीच्या काळात त्यांनी स्वातंत्र्य चळवळीतील सहभागाप्रमाणे सहभाग नोंदवला. शिक्षक संघटनेत ते सक्रीय होते. या प्रवासात त्यांनी सत्याग्रह करून तुरुंगवासही पत्करला. अकोले तालुक्यातील कॉ. दत्ता देशमुख आणि नंतर बी. के. देशमुख यांच्या सहवासातून कम्युनिस्ट चळवळीशीही त्यांनी सहभाग नोंदवला. पू. हरिभाऊ फडके यांच्या सहवासात म. गांधी यांच्या विचारांचाही संस्कार त्यांच्यावर झाला. या सर्व प्रवासात त्यांच्या मनात राष्ट्रीय आणि वैश्विक मानवतेचे विचार विकसित झाले. त्याच जाणिवेने त्यांचे कार्य सुरू आहे. 1979 पासून त्यांनी ‘वनवासी कल्याण आश्रम’, ‘राष्ट्र सेवा मंडळ’, ‘शिक्षकी पेशा’ आणि अलिकडे ‘आनंदोत्सव चॅरिटेबल ट्रस्ट’ या माध्यमातून त्यांचे सामाजिक व सांस्कृतिक कार्य सुरूच आहे. या सर्व कार्यात त्यांच्या पत्नी सौ. उषा सहस्रबुद्धे यांनी सर्व स्तरांवर मनःपूर्वक सक्रीय सहभाग नोंदविला.
